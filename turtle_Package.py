import turtle

C_Turtle=turtle.Turtle()
def square():
  
  C_Turtle.forward(100)
  C_Turtle.right(90)
  C_Turtle.forward(100)
  C_Turtle.right(90)
  C_Turtle.forward(100)
  C_Turtle.right(90)
  C_Turtle.forward(100)

while(0<5):
    square()
    C_Turtle.forward(100)
    square()
    C_Turtle.forward(100)
    square()
    C_Turtle.forward(100)
    square()
